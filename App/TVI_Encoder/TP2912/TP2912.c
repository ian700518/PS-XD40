BYTE Det_TVI_addr(){
	BYTE Reg_fe;
	BYTE i;

	Reg_fe = TP2912_ReadI2C(0x88,0xfe);

	for(i=0;i<=5;i++){
		if((Reg_fe == 0x28)||(Reg_fe == 0x29))
			return 0x88;
		else{
			Reg_fe = TP2912_ReadI2C(0x8a,0xfe);
			if((Reg_fe == 0x28)||(Reg_fe == 0x29))
				return 0x8a;
		}
	}
	return 0;//0x88;	
}

