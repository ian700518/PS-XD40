void Get_AHD_RxD(){
	BYTE bitpos;
	BYTE get_bit;
	BYTE line_cnt=0,byte_pos;
	BYTE Reg_RxD[20];
	BYTE *RxD;
	BYTE AHD_RxD[4]= {0,};
	BYTE AHD_bit=8;
	BYTE *pAHD_RxD;

	Read_Rxd(Reg_RxD);
	RxD = Reg_RxD;
	pAHD_RxD=AHD_RxD;

	while(AHD_bit--){
		switch(AHD_bit) {
			case 7:
				byte_pos = 2;
				bitpos = 6;
				break;
			case 6:
				byte_pos = 2;
				bitpos = 3;	
				break;
			case 5:
				byte_pos = 2;
				bitpos = 0;
				break;
			case 4:
				byte_pos = 3;
				bitpos = 5;
				break;
			case 3:
				byte_pos = 3;
				bitpos = 2;
				break;
			case 2:
				byte_pos = 4;
				bitpos = 7;
				break;
			case 1:
				byte_pos = 4;
				bitpos = 4;
				break;
			case 0:
				byte_pos = 4;
				bitpos = 1;
				break;
			default:
				break;
		}
		RxD += byte_pos;
		for(line_cnt=0;line_cnt<=3;line_cnt++){

			get_bit = getbits(*RxD,bitpos,1);
			*pAHD_RxD |= (get_bit << AHD_bit);

			RxD += 5;
			pAHD_RxD++;
		}

		pAHD_RxD -= line_cnt;
		RxD -= 5*line_cnt+byte_pos;
	}
	
	//Printf("\r\nAHD Data1:%02xh, Data2:%02xh, Data3:%02xh, Data4:%02xh\r\n",(WORD)*pAHD_RxD,(WORD)*(pAHD_RxD+1), (WORD)*(pAHD_RxD+2),(WORD)*(pAHD_RxD+3));
}
